#' Function to sum
#'
#' This function is good to sum.
#'
#' @param a  A number.
#' @param b It is another number
#'
#' @examples
#' sum(2, 3)
#' @export
#Exporta a função para NAMESPACE
somar <-  function(a, b) {
  sum(a, b)
}

